.MODEL SMALL
.STACK 100
.DATA
INPUT1 DB 'COLOR$'  
INPUT2 DB 'COLOUR$'
RESULT DB ?  ;STORED IN BL


.CODE   
MOV AX,@DATA
MOV DS,AX
MOV ES,AX

CLD
MOV SI, OFFSET INPUT1 ; DI = INPUT1 OFFSET
MOV DI, OFFSET INPUT2 ; SI = INPUT2 OFFSET
MOV CX, 7 ; load the counter  
REPE CMPSB
JNE MISMATCH


MATCH: 
MOV BL,1
MOV RESULT,BL
JMP EXIT

MISMATCH:
DEC SI 
DEC DI  
MOV BL,0
MOV RESULT,BL

EXIT:
MOV AX,4C00H
INT 21H

END

     
