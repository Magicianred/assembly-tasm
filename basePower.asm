.MODEL SMALL
.STACK 100
.DATA  
BASE DB 2h
POWER DB 3h
RESULT DD ?     
.CODE
MOV AX,@DATA
MOV DS,AX

XOR AX,AX
MOV AL,BASE
MOV BL,POWER
LABEL2: 
SHL AL,1
RCL DL,1
JA LABEL1  
LABEL1:
DEC BL 
CMP BL,1 ;IF POWER=1, THE RESULT IS CALCULATED 
MOV RESULT,AX
JNE LABEL2
   
MOV AX,4C00H
INT 21H 
END